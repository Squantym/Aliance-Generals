// ---------- ГОСПИТАЛЬ ----------
// Полное восстановление здоровья за доллары. Цена растёт с уровнем и
// сделана недешёвой: lvl 1 — $5K, lvl 50 — ~$1.2M, lvl 100 — ~$6.3M,
// lvl 200 — ~$22M, lvl 300 — ~$47M. Формула: 5000 × L^1.8.
const HOSPITAL = {
  basePrice: 5000,
  growthPow: 1.8,
};
function hospitalPrice(level: number): number {
  return Math.max(5000, Math.round(HOSPITAL.basePrice * Math.pow(level, HOSPITAL.growthPow) / 100) * 100);
}

