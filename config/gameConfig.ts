// Шаги: время в минутах зависит от уровня конфликта (растёт с прогрессом)
// energy = base + индекс_конфликта × 2 + индекс_операции × 1
// timeMin = base + индекс_операции × 5
// xpReward = базовый_для_конфликта × шаг (растёт по мере продвижения)
function buildConflictOperations(conflict: any, conflictIdx: number): any {
  const ops: any[] = [];
  for (let opIdx = 0; opIdx < conflict.ops; opIdx++) {
    const opName = SPEC_OP_NAMES[opIdx];
    const steps: any[] = [];
    for (let stepIdx = 0; stepIdx < 3; stepIdx++) {
      const energy = 4 + conflictIdx * 2 + opIdx * 1 + stepIdx;
      const timeMin = 5 + opIdx * 3 + stepIdx * 4 + conflictIdx * 2;
      // Опыт снижен в 3 раза относительно прежней версии — спецоперации
      // больше не основной источник опыта, упор смещён на бои.
      const baseXp = 3 + conflictIdx * 32;
      const xp = Math.round((baseXp + opIdx * 3 + stepIdx * 5) / 3);
      // Уровень, на котором примерно доступен этот шаг (уровень доступности миссии)
      const stepLevel = conflict.minLevel + opIdx + stepIdx;
      // Деньги = стоимость N единиц техники НА УРОВНЕ МИССИИ, где N растёт
      // от 10 (первый шаг первой операции) до 50 (последний шаг последней),
      // операция весит больше шага. Т.е. чем выше шаг/сложность — тем больше.
      const opFrac = conflict.ops > 1 ? opIdx / (conflict.ops - 1) : 0;
      const stepFrac = stepIdx / 2;
      const nUnits = Math.round(10 + 40 * (opFrac * 0.75 + stepFrac * 0.25)); // 10..50
      const money = Math.round(minUnitPriceAtLevel(stepLevel) * nUnits);
      // Ускорение шага: 10 золота за каждый час длительности (мин. 1).
      const boostGold = Math.max(1, Math.round((timeMin / 60) * 10));
      // Требования: сила армии, уровень и НАЛИЧИЕ ТЕХНИКИ соответствующего
      // уровня (нужно владеть N единиц техники, доступной ~на уровне миссии).
      const powerReq = Math.round(50 * Math.pow(1.6, conflictIdx) + opIdx * 30 + stepIdx * 20);
      // Требуемый уровень техники ЗАВИСИТ от уровня операции: на низких уровнях
      // допускаем зазор до 15 (новичку хватает доступной техники), а к высоким
      // уровням зазор сужается к 0 — нужна техника СООТВЕТСТВУЮЩЕГО уровня.
      const gap = Math.round(15 * (1 - Math.min(1, stepLevel / 250)));
      const unitReqLevel = Math.max(1, stepLevel - gap);
      const unitReqCount = 5 + conflictIdx * 3 + opIdx * 2 + stepIdx;
      steps.push({
        idx: stepIdx,
        name: SPEC_OP_STEPS[stepIdx],
        energy, timeMin,
        xp, money, boostGold,
        require: {
          power: powerReq,
          level: stepLevel,
          units: { minLevel: unitReqLevel, count: unitReqCount },
        },
      });
    }
    ops.push({
      idx: opIdx,
      id: conflict.id + '_' + opIdx,
      name: opName,
      img: SPEC_OP_SLUGS[opIdx] || null, // слаг картинки /img/conflicts/{confId}/{img}.webp
      steps,
    });
  }
  return ops;
}
