// на заданном уровне игрока. Используется для расчёта минимальной
// гарантированной награды за бой — игрок должен суметь купить хотя бы
// 10 единиц актуальной техники после одной атаки.
function minUnitPriceAtLevel(level: number): number {
  const candidates = UNITS.filter((x) => x.type === 'ground' && x.unlock <= level);
  if (!candidates.length) return UNITS[0].price;
  return candidates[candidates.length - 1].price;
}

// Цена САМОЙ ДОРОГОЙ доступной на уровне техники (по всем типам) — для шахт:
// стоимость постройки и денежные награды считаются в «единицах» этой техники.
function maxUnitPriceAtLevel(level: number): number {
  let max = 0;
  for (const x of UNITS) if (x.unlock <= level && x.price > max) max = x.price;
  return max || UNITS[0].price;
}

