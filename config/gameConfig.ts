// Расчёт текущей атаки/защиты секретной разработки для конкретного игрока:
//   до 50 уровня — базовое значение,
//   с 51 уровня — +1% за каждый уровень сверх 50,
//   плюс +0.5% за каждую сверхсекретную в коллекции (только на секретные!)
function secretLevelMul(user: any): number {
  const levelPart = user.level > 50 ? 1 + (user.level - 50) * 0.01 : 1;
  const superPart = 1 + (user.superSecret || 0) * 0.005;
  return levelPart * superPart;
}
function secretAtk(user: any, def: any): number { return Math.round(def.atk * secretLevelMul(user)); }
function secretDef(user: any, def: any): number { return Math.round(def.def * secretLevelMul(user)); }

