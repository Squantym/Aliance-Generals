// ---------- Опыт и уровни ----------
// Кусочная кривая. Базовые суммы УВЕЛИЧЕНЫ НА 50% относительно прежней
// версии (было ~1 000 000 суммарно, стало ~1 500 000):
//   - уровни 1..5: множитель ×1.5 от прежних 25,45,75,115,165
//   - уровни 6..99: рост до ~1529 (сумма сегмента ≈ 90 000)
//   - уровни 100..199: рост до ~5600 (сумма ≈ 360 000)
//   - уровни 200..299: рост до ~14313 (сумма ≈ 1 050 000)
// Монотонная: каждый следующий уровень дороже предыдущего.
// Опыт для перехода с `level` на `level+1`.
// Кривая откалибрована так, чтобы суммарный опыт по диапазонам был:
//   уровни 1–100   = 250 000  (внутри: 1–30 легко, 31–50 сложнее, 51–100 обычно)
//   уровни 101–200 = 1 000 000
//   уровни 201–300 = 8 000 000
// Внутри каждого диапазона требование растёт линейно — без резких скачков.
function xpToNext(level: number): number {
  // Зоны: [начальный уровень, конечный, опыт на старте зоны, опыт в конце зоны]
  const ZONES = [
    [1,   30,    333,   1000],   // 0–30: новичкам легко
    [31,  50,   1594,   2656],   // 31–50: чуть сложнее
    [51,  100,  2679,   4821],   // 51–100: обычные правила
    [101, 200,  6667,  13333],   // 101–200
    [201, 300, 24407, 135593],   // 201–300
  ];
  if (level < 1) return ZONES[0][2];
  for (const [s, e, xs, xe] of ZONES) {
    if (level >= s && level <= e) {
      if (e === s) return xs;
      return Math.round(xs + (level - s) * (xe - xs) / (e - s));
    }
  }
  // Выше максимума — отдаём значение последней зоны
  return ZONES[ZONES.length - 1][3];
}

