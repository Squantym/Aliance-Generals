// ---------- Генерация каталога техники ----------
// Целевые значения:
//   - стартовая цена ~$30,000 для всех типов
//   - финальная цена тира 100 ~ $47 миллиардов
//   - сумма atk+def примерно одинакова у всех типов на одном уровне (~100 на старте)
//   - наземная: сбалансированная 50/50 → финал ~5050/5050
//   - воздушная: упор на атаку 70/30 → финал ~7070/3030 (хищник)
//   - морская: упор на защиту 30/70 → финал ~3030/7070 (крепость)
//
// Формула роста статов: ((i+4)/4)^1.42 — на тире 100 ≈ 101x.
// Прирост на старте +37% за тир, плавно падает до +1.4% к финалу.
// Формула роста цены: 1.155^i — на тире 100 ≈ 1.57M x.
function unitStatMul(i) {
  return Math.pow((i + 4) / 4, 1.42);
}
function buildUnitCatalog() {
  const out = [];
  const defs = [
    // [тип, имена, базовая атака, базовая защита, базовая цена]
    ['ground', GROUND_NAMES, 50, 50, 30000],   // баланс
    ['air',    AIR_NAMES,    70, 30, 30000],   // упор на атаку
    ['sea',    SEA_NAMES,    30, 70, 30000],   // упор на защиту
  ];
  defs.forEach(([type, names, a, d, p], ti) => {
    names.forEach((name, i) => {
      const kStat = unitStatMul(i);
      const kPrice = Math.pow(1.155, i);
      // Округляем цену до удобных значений: тысячи в начале, миллионы дальше
      let price = p * kPrice;
      if (price >= 1e9) price = Math.round(price / 1e7) * 1e7;     // до 10 млн
      else if (price >= 1e6) price = Math.round(price / 1e4) * 1e4; // до 10 тыс
      else if (price >= 1e3) price = Math.round(price / 100) * 100; // до сотни
      else price = Math.round(price);
      out.push({
        id: `${type}_${i + 1}`,
        type, name,
        unlock: (ti + 1) + i * 3,
        attack: Math.max(1, Math.round(a * kStat)),
        defense: Math.max(1, Math.round(d * kStat)),
        price,
        // Содержание ~0.3% от цены за час; на топовых юнитах это значительная сумма
        upkeep: Math.max(50, Math.round((price * 0.003) / 100) * 100),
      });
    });
  });
  return out;
}
